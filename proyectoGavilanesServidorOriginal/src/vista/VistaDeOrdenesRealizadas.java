    private void visualizarMiBitacoraGeneral() {
        ConexionBD objCBD = new ConexionBD();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("NUM. ORDEN");
        modelo.addColumn("NOMBRE CLIENTE");
        modelo.addColumn("NOMBRE PRODUCTO");
        modelo.addColumn("CANTIDAD");
        modelo.addColumn("PRECIO ORDEN");

        jTableMiBitacora.setModel(modelo);
        String[] datos = new String[5];

        objCBD.realizarConexion();
        ArrayList instruccionBD = new ArrayList();
        instruccionBD.add("{call sp_consultarBitacora90()}");
        ResultSet cdr = objCBD.ejecutarConsulta(instruccionBD);
        try {
            while (cdr.next()) {
                datos[0] = cdr.getString("id_ordenC");
                datos[1] = cdr.getString("nombre_cliente_llamada");
                datos[2] = cdr.getString("nombreP");
                datos[3] = cdr.getString("cantidad");
                datos[4] = cdr.getString("precioOrden");

                modelo.addRow(datos);
            }
            jTableMiBitacora.setModel(modelo);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "No se pudo realizar la consulta" + ex, "ERROR", JOptionPane.ERROR_MESSAGE);

        } finally {

            objCBD.cerrarConexion();
        }
    }

    private void BusquedaFiltrada(String texto) {
        ConexionBD objCBD = new ConexionBD();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("NUM. ORDEN");
        modelo.addColumn("NOMBRE CLIENTE");
        modelo.addColumn("NOMBRE PRODUCTO");
        modelo.addColumn("CANTIDAD");
        modelo.addColumn("PRECIO ORDEN");
        jTableMiBitacora.setModel(modelo);
        String[] datos = new String[5];

        objCBD.realizarConexion();
        ArrayList instruccionBD = new ArrayList();
        instruccionBD.add("{call sp_consultarMisOrdenesRealizadas(?)}");
        instruccionBD.add(texto);
        ResultSet cdr = objCBD.ejecutarConsulta(instruccionBD);
        try {
            while (cdr.next()) {
                datos[0] = cdr.getString("id_ordenC");
                datos[1] = cdr.getString("nombre_cliente_llamada");
                datos[2] = cdr.getString("nombreP");
                datos[3] = cdr.getString("cantidad");
                datos[4] = cdr.getString("precioOrden");

                modelo.addRow(datos);
            }
            jTableMiBitacora.setModel(modelo);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "No se pudo realizar la consulta" + ex, "ERROR", JOptionPane.ERROR_MESSAGE);
        } finally {

            objCBD.cerrarConexion();
        }

    }
