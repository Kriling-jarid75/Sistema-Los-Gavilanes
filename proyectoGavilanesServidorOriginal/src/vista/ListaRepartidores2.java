    private void visualizarRegistrosRepartidores() {
        
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("NOMBRE REPARTIDORES");
        modelo.addColumn("APELLIDO PATERNO");
        modelo.addColumn("APELLIDO MATERNO");

        jTableRepartidores.setModel(modelo);
        String[] datos = new String[3];

        
        objCBD.realizarConexion();
        ArrayList instruccionBD = new ArrayList();
        instruccionBD.add("{call sp_consultarRepartidoresTabla3()}");
        ResultSet cdr = objCBD.ejecutarConsulta(instruccionBD);
        try {
            while (cdr.next()) {
                datos[0] = cdr.getString("nombreRepartidor");
                datos[1] = cdr.getString("apellidoPat");
                datos[2] = cdr.getString("apellidoMat");

                modelo.addRow(datos);

            }
            jTableRepartidores.setModel(modelo);
             objCBD.cerrarConexion();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "No se pudo realizar la consulta" + ex, "ERROR", JOptionPane.ERROR_MESSAGE);

        }finally {

            objCBD.cerrarConexion();
        }
    }
